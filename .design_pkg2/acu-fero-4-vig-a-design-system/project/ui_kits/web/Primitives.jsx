// ---- Button ----
function Button({ children, variant = 'primary', icon, full, onClick, disabled, type }) {
  const cls = ['btn', `btn-${variant}`, full && 'btn-full'].filter(Boolean).join(' ');
  return (
    <button type={type ?? 'button'} className={cls} onClick={onClick} disabled={disabled}>
      {icon ? icon : null}
      {children}
    </button>
  );
}

// ---- Badge ----
function Badge({ children, tone = 'gray', icon }) {
  return (
    <span className={`badge badge-${tone}`}>
      {icon ? icon : null}
      {children}
    </span>
  );
}

// ---- Banner (severity strip under the appbar) ----
function Banner({ kind, children }) {
  return <div className={`banner banner-${kind}`}>{children}</div>;
}

// ---- Card wrappers ----
function Card({ children, size = 'sm', ...rest }) {
  const cls = size === 'lg' ? 'card-lg' : 'card-base';
  return <div className={cls} {...rest}>{children}</div>;
}

// ---- Field wrappers ----
function FieldLabel({ children, required }) {
  return (
    <label className="field-label">
      {children} {required && <span className="field-required">*</span>}
    </label>
  );
}

Object.assign(window, { Icon, Button, Badge, Banner, Card, FieldLabel });
